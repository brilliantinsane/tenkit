import { spawn } from 'node:child_process';

import fs from 'fs-extra';
import { resolve } from 'pathe';

import { generateProject, type GenerateProjectConfig } from './generator';
import { writeProject, type WriteProjectOverwriteMode, type WriteProjectResult } from './writer';

export type GeneratedProjectGitMode = false | 'init' | 'commit';

export type RunGenerationProofOptions = GenerateProjectConfig & {
  targetDir: string;
  force?: boolean;
  git?: boolean | GeneratedProjectGitMode;
  workspaceRoot?: string;
};

export type RunWhiteLabelGenerationProofOptions = Omit<RunGenerationProofOptions, 'setupType'>;

export type RunGenerationProofResult = WriteProjectResult & {
  gitInitialized: boolean;
  gitCommitted: boolean;
  gitSkippedBecauseTargetWasNotEmpty: boolean;
};

export type RunWhiteLabelGenerationProofResult = RunGenerationProofResult;

function runGitInit(cwd: string): Promise<void> {
  return runGit(cwd, ['init']);
}

function runGit(cwd: string, args: string[]): Promise<void> {
  return new Promise((resolveGit, reject) => {
    const child = spawn('git', args, {
      cwd,
      stdio: 'ignore',
    });

    child.on('error', reject);
    child.on('close', (code) => {
      if (code === 0) {
        resolveGit();
        return;
      }

      reject(new Error(`git ${args.join(' ')} failed with exit code ${code ?? 'unknown'}.`));
    });
  });
}

function normalizeGitMode(git: RunGenerationProofOptions['git']): GeneratedProjectGitMode {
  if (git === false) {
    return false;
  }

  if (git === 'init' || git === 'commit') {
    return git;
  }

  return 'commit';
}

async function isExistingTargetDirectoryEmpty(targetDir: string): Promise<boolean> {
  if (!(await fs.pathExists(targetDir))) {
    return true;
  }

  const targetStats = await fs.stat(targetDir);

  if (!targetStats.isDirectory()) {
    return false;
  }

  return (await fs.readdir(targetDir)).length === 0;
}

export async function commitInitialGitSnapshot(targetDir: string) {
  await runGit(targetDir, ['add', '--all']);
  await runGit(targetDir, [
    'commit',
    '-m',
    'Initial commit\n\nGenerated by Tenkit Template generator.',
  ]);
}

export async function tryInitializeGitRepository(targetDir: string): Promise<boolean> {
  try {
    await runGitInit(targetDir);
    return true;
  } catch {
    return false;
  }
}

export async function tryCommitInitialGitSnapshot(targetDir: string): Promise<boolean> {
  try {
    await commitInitialGitSnapshot(targetDir);
    return true;
  } catch {
    return false;
  }
}

export async function runGenerationProof(
  options: RunGenerationProofOptions,
): Promise<RunGenerationProofResult> {
  const tree = generateProject({
    setupType: options.setupType,
    projectName: options.projectName,
    packageName: options.packageName,
    packageManager: options.packageManager,
  });
  const overwrite: WriteProjectOverwriteMode = options.force ? 'always' : 'never';
  const targetWasEmpty = await isExistingTargetDirectoryEmpty(options.targetDir);

  const result = await writeProject({
    targetDir: options.targetDir,
    tree,
    overwrite,
    forbiddenTargetRoots: options.workspaceRoot ? [options.workspaceRoot] : [],
  });
  const gitMode = normalizeGitMode(options.git);
  let gitInitialized = false;
  let gitCommitted = false;
  const gitSkippedBecauseTargetWasNotEmpty = Boolean(gitMode && !targetWasEmpty);

  if (gitMode && targetWasEmpty) {
    gitInitialized = await tryInitializeGitRepository(result.targetDir);

    if (gitInitialized && gitMode === 'commit') {
      gitCommitted = await tryCommitInitialGitSnapshot(result.targetDir);
    }
  }

  return {
    ...result,
    gitInitialized,
    gitCommitted,
    gitSkippedBecauseTargetWasNotEmpty,
  };
}

export async function runWhiteLabelGenerationProof(
  options: RunWhiteLabelGenerationProofOptions,
): Promise<RunWhiteLabelGenerationProofResult> {
  return runGenerationProof({
    ...options,
    setupType: 'white-label-apps',
  });
}
